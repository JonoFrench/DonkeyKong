        switch assetType {
        case .girder:
            ImageResource(name: "Girder", bundle: .main)
        case .ladder:
            ImageResource(name: "Ladder", bundle: .main)
        case .blank:
            ImageResource(name: "Lives", bundle: .main)
        case .oilBL:
            ImageResource(name: "oilBL", bundle: .main)
        case .oilBR:
            ImageResource(name: "oilBR", bundle: .main)
        case .oilTL:
            ImageResource(name: "oilTL", bundle: .main)
        case .oilTR:
            ImageResource(name: "oilTR", bundle: .main)
        }
    }
    
    init(assetType: AssetType, assetOffset: Double) {
        self.assetType = assetType
        self.assetOffset = assetOffset
    }
    
